using SMTPRelay.Model.DB;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SMTPRelay.Model.Query
{
    public class qryGetAllIPEndpoints : DatabaseQuery
    {
        public List<tblIPEndpoint> Results;
        public qryGetAllIPEndpoints()
        {
            Results = null;
            DoneSignal = new System.Threading.ManualResetEventSlim();
            Aborted = false;
        }

        public void SetResult(List<tblIPEndpoint> value)
        {
            Results = value;
            DoneSignal.Set();
        }

        public List<tblIPEndpoint> GetResult()
        {
            DoneSignal.Wait();
            DoneSignal.Dispose();
            if (Aborted)
            {
                throw new OperationCanceledException();
            }
            return Results;
        }
    }
}
